#3. Write a python program to read two numbers and find the sum of their cubes
# Input: Getting two numbers from the user 
num1 = float(input("Enter the first number: ")) 
num2 = float(input("Enter the second number: ")) 
# Calculating the sum of their cubes 
sum_of_cubes = num1**3 + num2**3 
# Output: Displaying the result 
print("The sum of the cubes of", num1, "and", num2, "is:", sum_of_cubes)